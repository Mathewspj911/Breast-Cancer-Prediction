import streamlit as st
import pandas as pd
import joblib

# Load model
model = joblib.load("breast_cancer_model.pkl")

st.title("Breast Cancer Prediction System")

st.write("Enter the tumor measurements below:")

# User Inputs
user_inputs = {}

for feature in model.feature_names_in_:
    user_inputs[feature] = st.number_input(
        feature,
        value=0.0,
        format="%.6f"
    )

# Predict Button
if st.button("Predict"):

    input_df = pd.DataFrame([user_inputs])

    prediction = model.predict(input_df)[0]
    probability = model.predict_proba(input_df)

    st.subheader("Prediction Result")

    if prediction == 1:
        st.error("⚠️ Prediction: Malignant (Cancerous)")
    else:
        st.success("✅ Prediction: Benign (Non-Cancerous)")

    st.subheader("Prediction Probability")

    st.write(
        f"Benign Probability: {probability[0][0]*100:.2f}%"
    )

    st.write(
        f"Malignant Probability: {probability[0][1]*100:.2f}%"
    )

    st.info(
        "This prediction is generated by a machine learning model and should not be considered a medical diagnosis."
    )